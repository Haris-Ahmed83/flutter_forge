// --- 5. HomePage Widget (Displays a list of notes) ---
class HomePage extends StatefulWidget {
  const HomePage({super.key});

  @override
  State<HomePage> createState() => _HomePageState();
}

class _HomePageState extends State<HomePage> {
  late Box<Note> _noteBox; // Reference to the Hive box storing notes.

  @override
  void initState() {
    super.initState();
    _noteBox = Hive.box<Note>('notes'); // Get the opened box instance.

    // Add some sample data if the notes box is empty on first launch.
    if (_noteBox.isEmpty) {
      _addSampleNotes();
    }
  }

  // Adds a couple of default notes to demonstrate the app's functionality.
  void _addSampleNotes() {
    final uuid = const Uuid(); // Used to generate unique IDs for notes.
    _noteBox.add(Note(
      id: uuid.v4(),
      title: 'Welcome to Notes App!',
      content: 'This is your first note. Tap to edit it, or use the + button to add a new one.',
      timestamp: DateTime.now().subtract(const Duration(minutes: 10)),
    ));
    _noteBox.add(Note(
      id: uuid.v4(),
      title: 'Shopping List',
      content: 'Milk, Eggs, Bread, Butter, Fruits, Vegetables',
      timestamp: DateTime.now().subtract(const Duration(minutes: 5)),
    ));
  }

  // Navigates to the NoteDetailScreen to add a new note or edit an existing one.
  Future<void> _navigateToNoteDetail({Note? note}) async {
    await Navigator.push(
      context,
      MaterialPageRoute(
        builder: (context) => NoteDetailScreen(note: note),
      ),
    );
    // After returning from NoteDetailScreen, refresh the UI to reflect any changes.
    setState(() {});
  }

  // Deletes a note from the Hive box.
  void _deleteNote(Note note) {
    // When a Note object is retrieved from a Hive box, it automatically
    // gains a `.delete()` method for direct removal from the box.
    note.delete();
    setState(() {}); // Refresh the UI after deletion.
  }

  // Formats a DateTime object into a more readable string.
  String _formatDateTime(DateTime dateTime) {
    return '${dateTime.day.toString().padLeft(2, '0')}/${dateTime.month.toString().padLeft(2, '0')}/${dateTime.year} ${dateTime.hour.toString().padLeft(2, '0')}:${dateTime.minute.toString().padLeft(2, '0')}';
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('My Notes'),
      ),
      body: ValueListenableBuilder<Box<Note>>(
        // ValueListenableBuilder automatically rebuilds its child widget
        // whenever changes occur in the '_noteBox', ensuring the UI is always up-to-date.
        valueListenable: _noteBox.listenable(),
        builder: (context, box, _) {
          final notes = box.values.toList().cast<Note>();
          // Sort notes by timestamp in descending order (newest first).
          notes.sort((a, b) => b.timestamp.compareTo(a.timestamp));

          if (notes.isEmpty) {
            return Center(
              child: Padding(
                padding: const EdgeInsets.all(24.0),
                child: Text(
                  'No notes yet! Tap the + button to add one.',
                  style: Theme.of(context).textTheme.titleMedium,
                  textAlign: TextAlign.center,
                ),
              ),
            );
          }

          return ListView.builder(
            padding: const EdgeInsets.all(8.0),
            itemCount: notes.length,
            itemBuilder: (context, index) {
              final note = notes[index];
              return Card(
                margin: const EdgeInsets.symmetric(vertical: 6.0, horizontal: 4.0),
                elevation: 2.0, // Adds a subtle shadow to the card.
                child: ListTile(
                  contentPadding: const EdgeInsets.all(12.0),
                  title: Text(
                    note.title,
                    style: Theme.of(context).textTheme.titleLarge,
                    maxLines: 1,
                    overflow: TextOverflow.ellipsis, // Truncates long titles.
                  ),
                  subtitle: Padding(
                    padding: const EdgeInsets.only(top: 4.0),
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          note.content,
                          style: Theme.of(context).textTheme.bodyMedium,
                          maxLines: 2,
                          overflow: TextOverflow.ellipsis, // Truncates long content.
                        ),
                        const SizedBox(height: 8.0),
                        Text(
                          _formatDateTime(note.timestamp),
                          style: Theme.of(context).textTheme.bodySmall?.copyWith(
                            color: Theme.of(context).colorScheme.outline, // Use outline color for subtle timestamp.
                          ),
                        ),
                      ],
                    ),
                  ),
                  onTap: () => _navigateToNoteDetail(note: note), // Tapping edits the note.
                  trailing: IconButton(
                    icon: Icon(Icons.delete_outline, color: Theme.of(context).colorScheme.error),
                    onPressed: () => _deleteNote(note), // Deletes the note.
                    tooltip: 'Delete Note',
                  ),
                ),
              );
            },
          );
        },
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: () => _navigateToNoteDetail(), // Tapping adds a new note.
        tooltip: 'Add New Note',
        child: const Icon(Icons.add),
      ),
    );
  }
}

// --- 6. NoteDetailScreen Widget (For adding new notes or editing existing ones) ---
class NoteDetailScreen extends StatefulWidget {
  final Note? note; // Optional: If provided, we are editing an existing note.

  const NoteDetailScreen({super.key, this.note});

  @override
  State<NoteDetailScreen> createState() => _NoteDetailScreenState();
}

class _NoteDetailScreenState extends State<NoteDetailScreen> {
  final _formKey = GlobalKey<FormState>(); // Key for form validation.
  late TextEditingController _titleController;
  late TextEditingController _contentController;
  late Box<Note> _noteBox;

  @override
  void initState() {
    super.initState();
    _noteBox = Hive.box<Note>('notes');
    // Initialize controllers with existing note data if editing, otherwise empty.
    _titleController = TextEditingController(text: widget.note?.title ?? '');
    _contentController = TextEditingController(text: widget.note?.content ?? '');
  }

  @override
  void dispose() {
    _titleController.dispose();
    _contentController.dispose();
    super.dispose();
  }

  // Saves a new note or updates an existing one in the Hive box.
  void _saveNote() {
    if (_formKey.currentState!.validate()) {
      final String title = _titleController.text.trim();
      final String content = _contentController.text.trim();

      if (widget.note == null) {
        // Create new note
        final uuid = const Uuid();
        _noteBox.add(Note(
          id: uuid.v4(),
          title: title,
          content: content,
          timestamp: DateTime.now(),
        ));
      } else {
        // Update existing note
        // Modifying fields of a retrieved Hive object and calling .save()
        // on it automatically persists the changes to the box.
        widget.note!.title = title;
        widget.note!.content = content;
        widget.note!.timestamp = DateTime.now(); // Update timestamp on edit.
        widget.note!.save();
      }
      Navigator.pop(context); // Go back to the home page after saving.
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.note == null ? 'Add New Note' : 'Edit Note'),
        actions: [
          // Display a delete button on the app bar only when editing an existing note.
          if (widget.note != null)
            IconButton(
              icon: Icon(Icons.delete_forever, color: Theme.of(context).colorScheme.error),
              onPressed: () {
                // Show a confirmation dialog before deleting.
                showDialog(
                  context: context,
                  builder: (context) => AlertDialog(
                    title: const Text('Delete Note?'),
                    content: const Text('Are you sure you want to delete this note permanently?'),
                    actions: [
                      TextButton(
                        onPressed: () => Navigator.pop(context), // Dismiss dialog.
                        child: const Text('Cancel'),
                      ),
                      FilledButton(
                        onPressed: () {
                          widget.note!.delete(); // Delete the note.
                          Navigator.pop(context); // Dismiss dialog.
                          Navigator.pop(context); // Go back to home page.
                        },
                        style: FilledButton.styleFrom(backgroundColor: Theme.of(context).colorScheme.error),
                        child: const Text('Delete'),
                      ),
                    ],
                  ),
                );
              },
              tooltip: 'Delete Note',
            ),
        ],
      ),
      body: Form(
        key: _formKey, // Associates the form with the validation key.
        child: Padding(
          padding: const EdgeInsets.all(16.0),
          child: Column(
            children: [
              TextFormField(
                controller: _titleController,
                decoration: InputDecoration(
                  labelText: 'Title',
                  border: const OutlineInputBorder(),
                  hintText: 'Enter note title',
                  // Apply Material 3 focused border styling.
                  floatingLabelStyle: TextStyle(color: Theme.of(context).colorScheme.primary),
                  focusedBorder: OutlineInputBorder(
                    borderSide: BorderSide(color: Theme.of(context).colorScheme.primary, width: 2.0),
                  ),
                ),
                style: Theme.of(context).textTheme.titleLarge,
                validator: (value) {
                  if (value == null || value.trim().isEmpty) {
                    return 'Title cannot be empty';
                  }
                  return null;
                },
                maxLines: 1, // Restricts title to a single line.
              ),
              const SizedBox(height: 16.0),
              Expanded(
                child: TextFormField(
                  controller: _contentController,
                  decoration: InputDecoration(
                    labelText: 'Content',
                    border: const OutlineInputBorder(),
                    hintText: 'Write your note here...',
                    alignLabelWithHint: true, // Aligns the label to the top for multiline input.
                    // Apply Material 3 focused border styling.
                    floatingLabelStyle: TextStyle(color: Theme.of(context).colorScheme.primary),
                    focusedBorder: OutlineInputBorder(
                      borderSide: BorderSide(color: Theme.of(context).colorScheme.primary, width: 2.0),
                    ),
                  ),
                  style: Theme.of(context).textTheme.bodyMedium,
                  maxLines: null, // Allows the content field to have unlimited lines.
                  expands: true, // Allows the text field to expand vertically to fill available space.
                  textAlignVertical: TextAlignVertical.top, // Starts text at the top of the field.
                  validator: (value) {
                    if (value == null || value.trim().isEmpty) {
                      return 'Content cannot be empty';
                    }
                    return null;
                  },
                ),
              ),
              const SizedBox(height: 16.0),
              SizedBox(
                width: double.infinity, // Makes the button take full width.
                child: FilledButton.icon(
                  onPressed: _saveNote,
                  icon: const Icon(Icons.save),
                  label: const Text('Save Note'),
                  style: FilledButton.styleFrom(
                    padding: const EdgeInsets.symmetric(vertical: 12.0),
                    textStyle: Theme.of(context).textTheme.titleMedium,
                  ),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }
}
