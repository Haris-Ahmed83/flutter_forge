import 'package:flutter/material.dart';
import 'package:hive_flutter/hive_flutter.dart';
import 'package:uuid/uuid.dart';

// --- 1. Note Model Definition ---
// In a typical project, this class would have `@HiveType` and `@HiveField` annotations,
// and 'build_runner' would generate a 'note.g.dart' file with the necessary adapter.
// For this single-file example, we manually define the adapter below to avoid
// requiring a code generation step that cannot be included in a single file.
class Note {
  String id;
  String title;
  String content;
  DateTime timestamp;

  Note({
    required this.id,
    required this.title,
    required this.content,
    required this.timestamp,
  });
}

// --- 2. Manual Hive Type Adapter for Note ---
// This adapter manually defines how to serialize and deserialize a Note object
// for storage in Hive. It mimics the functionality of an adapter that would
// typically be generated by `build_runner`.
class NoteAdapter extends TypeAdapter<Note> {
  @override
  final int typeId = 0; // A unique ID for this type adapter within the app.

  @override
  Note read(BinaryReader reader) {
    final numOfFields = reader.readByte();
    final fields = <int, dynamic>{
      for (int i = 0; i < numOfFields; i++) reader.readByte(): reader.read(),
    };
    return Note(
      id: fields[0] as String,
      title: fields[1] as String,
      content: fields[2] as String,
      timestamp: fields[3] as DateTime,
    );
  }

  @override
  void write(BinaryWriter writer, Note obj) {
    writer
      ..writeByte(4) // Indicates the number of fields being written.
      ..writeByte(0) // Field index for 'id'
      ..write(obj.id)
      ..writeByte(1) // Field index for 'title'
      ..write(obj.title)
      ..writeByte(2) // Field index for 'content'
      ..write(obj.content)
      ..writeByte(3) // Field index for 'timestamp'
      ..write(obj.timestamp);
  }

  @override
  int get hashCode => typeId.hashCode;

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is NoteAdapter &&
          runtimeType == other.runtimeType &&
          typeId == other.typeId;
}

// --- 3. Main Application Entry Point ---
Future<void> main() async {
  // Ensures Flutter widgets are initialized before running the app.
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Hive database and register the custom adapter for the Note model.
  await Hive.initFlutter();
  Hive.registerAdapter(NoteAdapter());

  // Open the Hive box where all notes will be stored.
  await Hive.openBox<Note>('notes');

  // Run the Flutter application.
  runApp(const MyApp());
}

// --- 4. MyApp Widget (Root of the application) ---
class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
